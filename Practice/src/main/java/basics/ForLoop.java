package basics;

public class ForLoop {

	public static void main(String[] args) {
		//Syntax: for(initialization ; condition ; increment/ decrement)
		
		for(int j=0; j<=3; j++) {
			
			for(int i=0; i<=3; i++) {
				System.out.print("*");	
			}
			
			System.out.println();
		}
		

	}

}
