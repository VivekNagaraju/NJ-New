package basics;

public class DataTypes {
	
	static int a;
	static byte b;
	static short c;
	static long d;
	static float e;
	static double f;
	static char g;
	static boolean h;

	public static void main(String[] args) {
		
		a=10;
		
		System.out.print(a);
		System.out.print(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);

	}

}
