package basics;

public class TypeCasting {

	public static void main(String[] args) {
		int a = 4;
		float b = a;
		System.out.println(a);
		System.out.println(b);
		
		double c = 4.5;
		int d = (int) c;
		
		System.out.println(c);
		System.out.println(d);
		
		double e = (char) a;
		char f = 's';
		
		System.out.println(e);
		System.out.println(f);

	}

}
