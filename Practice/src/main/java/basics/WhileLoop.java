package basics;

public class WhileLoop {

	public static void main(String[] args) {
		// Syntax: While- Loop
//		initialization
//		while (condition) {
//			statements;
//			increment/decrement
//		}

		
		  int i = 0; 
		  while (i<0) { 
			  System.out.println("Hello"); 
			  i++; 
			  }
		 

		
		  int j = 0;
		  
		  do { 
			  System.out.println("Hello"); 
			  } while (j<0);
		 
	}

}
